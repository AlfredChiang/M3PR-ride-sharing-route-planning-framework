from __future__ import annotations

import argparse
from pathlib import Path

import matplotlib.pyplot as plt
import pandas as pd


def main() -> None:
    parser = argparse.ArgumentParser(description="Plot sweep results generated by scripts/run_sweep.py.")
    parser.add_argument("--csv", type=Path, required=True)
    parser.add_argument("--x", default="pickup_delay")
    parser.add_argument("--y", default="response_rate")
    parser.add_argument("--group", default="name")
    parser.add_argument("--out", type=Path, default=Path("results/figures/sweep_plot.png"))
    args = parser.parse_args()

    df = pd.read_csv(args.csv)
    fig, ax = plt.subplots()
    for group_value, sub in df.groupby(args.group):
        grouped = sub.groupby(args.x, as_index=False)[args.y].mean().sort_values(args.x)
        ax.plot(grouped[args.x], grouped[args.y], marker="o", label=str(group_value))
    ax.set_xlabel(args.x)
    ax.set_ylabel(args.y)
    ax.legend()
    fig.tight_layout()
    args.out.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(args.out, dpi=200)
    print(f"saved figure to {args.out}")


if __name__ == "__main__":
    main()
